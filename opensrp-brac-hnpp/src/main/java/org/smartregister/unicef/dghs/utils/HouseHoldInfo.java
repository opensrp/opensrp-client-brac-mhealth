package org.smartregister.unicef.dghs.utils;

public class HouseHoldInfo {
    private String houseHoldBaseEntityId;
    private String houseHoldName;
    private String houseHoldHeadId;
    private String primaryCaregiverId;
    private String houseHoldUniqueId;
    private String moduleId;

    public String getHouseHoldBaseEntityId() {
        return houseHoldBaseEntityId;
    }

    public void setHouseHoldBaseEntityId(String houseHoldBaseEntityId) {
        this.houseHoldBaseEntityId = houseHoldBaseEntityId;
    }

    public String getHouseHoldName() {
        return houseHoldName;
    }

    public void setHouseHoldName(String houseHoldName) {
        this.houseHoldName = houseHoldName;
    }

    public String getHouseHoldHeadId() {
        return houseHoldHeadId;
    }

    public void setHouseHoldHeadId(String houseHoldHeadId) {
        this.houseHoldHeadId = houseHoldHeadId;
    }

    public String getPrimaryCaregiverId() {
        return primaryCaregiverId;
    }

    public void setPrimaryCaregiverId(String primaryCaregiverId) {
        this.primaryCaregiverId = primaryCaregiverId;
    }

    public String getHouseHoldUniqueId() {
        return houseHoldUniqueId;
    }

    public void setHouseHoldUniqueId(String houseHoldUniqueId) {
        this.houseHoldUniqueId = houseHoldUniqueId;
    }

    public String getModuleId() {
        return moduleId;
    }

    public void setModuleId(String moduleId) {
        this.moduleId = moduleId;
    }
}
